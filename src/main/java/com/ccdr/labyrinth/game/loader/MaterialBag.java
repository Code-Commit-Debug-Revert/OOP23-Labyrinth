package com.ccdr.labyrinth.game.loader;

public class MaterialBag {

    private int requiredDiamond, requiredCoal;
    private int requiredCopper, requiredIron;
    private int requiredSilk, requiredWood;

    public void setRequiredDiamond(int requiredDiamond) {
        this.requiredDiamond = requiredDiamond;
    }

    public void setRequiredCoal(int requiredCoal) {
        this.requiredCoal = requiredCoal;
    }

    public void setRequiredCopper(int requiredCopper) {
        this.requiredCopper = requiredCopper;
    }

    public void setRequiredIron(int requiredIron) {
        this.requiredIron = requiredIron;
    }

    public void setRequiredSilk(int requiredSilk) {
        this.requiredSilk = requiredSilk;
    }

    public void setRequiredWood(int requiredWood) {
        this.requiredWood = requiredWood;
    }


    public int getRequiredDiamond() {
        return requiredDiamond;
    }

    public int getRequiredCoal() {
        return requiredCoal;
    }

    public int getRequiredCopper() {
        return requiredCopper;
    }

    public int getRequiredIron() {
        return requiredIron;
    }

    public int getRequiredSilk() {
        return requiredSilk;
    }

    public int getRequiredWood() {
        return requiredWood;
    }
}
